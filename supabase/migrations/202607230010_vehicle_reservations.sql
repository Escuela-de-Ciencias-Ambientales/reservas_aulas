create table if not exists public.vehicle_maintenance (
  id uuid primary key default gen_random_uuid(),
  vehicle_id bigint not null references public.vehicles(id) on delete restrict,
  starts_at timestamptz not null,
  ends_at timestamptz,
  category text not null check (category in ('oil_change', 'minor_repair', 'major_repair')),
  details text check (details is null or char_length(trim(details)) <= 240),
  active boolean not null default true,
  created_by uuid not null references public.profiles(id) on delete restrict,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (ends_at is null or starts_at < ends_at)
);

create table if not exists public.vehicle_reservations (
  id uuid primary key default gen_random_uuid(),
  vehicle_id bigint not null references public.vehicles(id) on delete restrict,
  user_id uuid not null references public.profiles(id) on delete restrict,
  responsible_name text not null check (char_length(trim(responsible_name)) between 3 and 100),
  starts_at timestamptz not null,
  ends_at timestamptz not null,
  destination text not null check (char_length(trim(destination)) between 2 and 160),
  objective text not null check (char_length(trim(objective)) between 3 and 240),
  additional_drivers text[] not null default '{}',
  status text not null default 'confirmed'
    check (status in ('confirmed', 'suspended_maintenance', 'cancelled')),
  maintenance_id uuid references public.vehicle_maintenance(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  cancelled_at timestamptz,
  check (starts_at < ends_at),
  check (cardinality(additional_drivers) <= 2),
  exclude using gist (
    vehicle_id with =,
    tstzrange(starts_at, ends_at, '[)') with &&
  ) where (status in ('confirmed', 'suspended_maintenance'))
);

create table if not exists public.vehicle_service_schedules (
  vehicle_id bigint primary key references public.vehicles(id) on delete cascade,
  current_mileage integer check (current_mileage is null or current_mileage >= 0),
  next_oil_date date,
  next_oil_mileage integer check (next_oil_mileage is null or next_oil_mileage >= 0),
  next_dekra_month date,
  notes text check (notes is null or char_length(trim(notes)) <= 240),
  updated_by uuid references public.profiles(id) on delete set null,
  updated_at timestamptz not null default now()
);

