create or replace function public.get_public_vehicle_overview()
returns table (
  id bigint, plate text, display_name text, make text, model text, model_year integer,
  image_path text, maintenance_category text, maintenance_starts_at timestamptz,
  maintenance_ends_at timestamptz, current_mileage integer,
  next_oil_date date, next_oil_mileage integer
)
language sql
stable
security definer
set search_path = public
as $$
  select v.id, v.plate, v.display_name, v.make, v.model, v.model_year, v.image_path,
    m.category, m.starts_at, m.ends_at,
    s.current_mileage, s.next_oil_date, s.next_oil_mileage
  from public.vehicles v
  left join lateral (
    select category, starts_at, ends_at from public.vehicle_maintenance
    where vehicle_id = v.id and active
      and tstzrange(starts_at, coalesce(ends_at, 'infinity'::timestamptz), '[)') @> now()
    order by starts_at desc limit 1
  ) m on true
  left join public.vehicle_service_schedules s on s.vehicle_id = v.id
  where v.active
  order by v.sort_order, v.plate;
$$;

create or replace function public.get_public_vehicle_calendar(p_from date, p_to date)
returns table (
  id uuid, vehicle_id bigint, starts_at timestamptz, ends_at timestamptz,
  responsible_name text, status text, event_type text, category text
)
language plpgsql
stable
security definer
set search_path = public
as $$
begin
  if p_to < p_from or p_to - p_from > 370 then
    raise exception using errcode = '22023', message = 'El rango solicitado no es válido';
  end if;
  return query
  select r.id, r.vehicle_id, r.starts_at, r.ends_at, r.responsible_name,
    r.status, 'reservation'::text, null::text
  from public.vehicle_reservations r
  where r.status <> 'cancelled'
    and r.starts_at < (p_to + 1)::timestamptz and r.ends_at > p_from::timestamptz
  union all
  select m.id, m.vehicle_id, m.starts_at, coalesce(m.ends_at, (p_to + 1)::timestamptz),
    null::text, 'maintenance'::text, 'maintenance'::text, m.category
  from public.vehicle_maintenance m
  where m.active and m.starts_at < (p_to + 1)::timestamptz
    and coalesce(m.ends_at, 'infinity'::timestamptz) > p_from::timestamptz;
end;
$$;

create or replace function public.admin_set_vehicle_maintenance(
  p_vehicle_id bigint, p_starts_at timestamptz, p_ends_at timestamptz,
  p_category text, p_details text
)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare new_id uuid;
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = 'Se requiere acceso administrativo';
  end if;
  if p_category not in ('oil_change', 'minor_repair', 'major_repair')
    or (p_ends_at is not null and p_starts_at >= p_ends_at) then
    raise exception using errcode = '22023', message = 'Los datos de mantenimiento no son válidos';
  end if;
  insert into public.vehicle_maintenance
    (vehicle_id, starts_at, ends_at, category, details, created_by)
  values
    (p_vehicle_id, p_starts_at, p_ends_at, p_category, nullif(trim(p_details), ''), auth.uid())
  returning id into new_id;
  update public.vehicle_reservations
  set status = 'suspended_maintenance', maintenance_id = new_id
  where vehicle_id = p_vehicle_id and status = 'confirmed'
    and tstzrange(starts_at, ends_at, '[)')
      && tstzrange(p_starts_at, coalesce(p_ends_at, 'infinity'::timestamptz), '[)');
  return new_id;
end;
$$;

create or replace function public.admin_close_vehicle_maintenance(p_id uuid, p_ends_at timestamptz)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = 'Se requiere acceso administrativo';
  end if;
  update public.vehicle_maintenance
  set active = false,
      ends_at = greatest(coalesce(p_ends_at, now()), starts_at + interval '1 minute')
  where id = p_id;
end;
$$;

create or replace function public.admin_resolve_vehicle_reservation(
  p_id uuid, p_action text, p_vehicle_id bigint default null
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare item public.vehicle_reservations;
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = 'Se requiere acceso administrativo';
  end if;
  select * into item from public.vehicle_reservations where id = p_id for update;
  if not found then raise exception using errcode = '22023', message = 'Reserva no encontrada'; end if;
  if p_action = 'cancel' then
    update public.vehicle_reservations set status = 'cancelled' where id = p_id;
  elsif p_action in ('reactivate', 'reassign') then
    if exists (
      select 1 from public.vehicle_maintenance m
      where m.vehicle_id = case when p_action = 'reassign' then coalesce(p_vehicle_id, item.vehicle_id) else item.vehicle_id end
        and m.active
        and tstzrange(m.starts_at, coalesce(m.ends_at, 'infinity'::timestamptz), '[)')
          && tstzrange(item.starts_at, item.ends_at, '[)')
    ) then
      raise exception using errcode = '23P01', message = 'El vehículo continúa bloqueado por mantenimiento en ese periodo';
    end if;
    update public.vehicle_reservations
    set vehicle_id = case when p_action = 'reassign' then coalesce(p_vehicle_id, vehicle_id) else vehicle_id end,
        status = 'confirmed', maintenance_id = null
    where id = p_id;
  else
    raise exception using errcode = '22023', message = 'Acción no válida';
  end if;
end;
$$;

create or replace function public.admin_upsert_vehicle_service(
  p_vehicle_id bigint, p_current_mileage integer, p_next_oil_date date,
  p_next_oil_mileage integer, p_next_dekra_month date, p_notes text
)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin() then
    raise exception using errcode = '42501', message = 'Se requiere acceso administrativo';
  end if;
  insert into public.vehicle_service_schedules
    (vehicle_id, current_mileage, next_oil_date, next_oil_mileage, next_dekra_month, notes, updated_by)
  values
    (p_vehicle_id, p_current_mileage, p_next_oil_date, p_next_oil_mileage,
     case when p_next_dekra_month is null then null else date_trunc('month', p_next_dekra_month)::date end,
     nullif(trim(p_notes), ''), auth.uid())
  on conflict (vehicle_id) do update set
    current_mileage = excluded.current_mileage,
    next_oil_date = excluded.next_oil_date,
    next_oil_mileage = excluded.next_oil_mileage,
    next_dekra_month = excluded.next_dekra_month,
    notes = excluded.notes,
    updated_by = auth.uid(),
    updated_at = now();
end;
$$;

