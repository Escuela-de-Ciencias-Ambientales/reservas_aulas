create or replace function public.enforce_teacher_limit()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.role = 'teacher' then
    if tg_op = 'INSERT' and (select count(*) from public.profiles where role = 'teacher') >= 50 then
      raise exception using errcode = '23514', message = 'Se alcanzó el máximo de 50 cuentas docentes';
    elsif tg_op = 'UPDATE' and old.role <> 'teacher' and (select count(*) from public.profiles where role = 'teacher') >= 50 then
      raise exception using errcode = '23514', message = 'Se alcanzó el máximo de 50 cuentas docentes';
    end if;
  end if;
  return new;
end;
$$;

create trigger profiles_enforce_teacher_limit
before insert or update of role on public.profiles
for each row execute function public.enforce_teacher_limit();

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, full_name, role)
  values (
    new.id,
    lower(coalesce(new.email, '')),
    coalesce(nullif(trim(new.raw_user_meta_data ->> 'full_name'), ''), split_part(coalesce(new.email, 'Docente'), '@', 1)),
    case when new.raw_user_meta_data ->> 'role' = 'admin' then 'admin'::public.user_role else 'teacher'::public.user_role end
  );
  return new;
end;
$$;

create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

create or replace function public.protect_reservation_fields()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  fixed_label text;
begin
  if new.reservation_date < current_date then
    raise exception using errcode = '22023', message = 'No se permiten reservas en fechas pasadas';
  end if;

  if extract(dow from new.reservation_date)::integer = 0 then
    raise exception using errcode = '22023', message = 'No se permiten reservas los domingos';
  end if;

  select fixed.label into fixed_label
  from public.fixed_occupancies fixed
  where fixed.classroom_id = new.classroom_id
    and new.reservation_date <= date '2026-11-14'
    and fixed.day_of_week = extract(dow from new.reservation_date)::integer
    and new.start_time < fixed.end_time
    and new.end_time > fixed.start_time
  limit 1;

  if fixed_label is not null then
    raise exception using errcode = '23P01', message = 'El horario coincide con una ocupación académica fija';
  end if;

  if tg_op = 'INSERT' then
    new.professor_name = (select full_name from public.profiles where id = new.user_id);
  else
    if not public.is_admin() and (
      new.user_id <> old.user_id
      or new.classroom_id <> old.classroom_id
      or new.reservation_date <> old.reservation_date
      or new.start_time <> old.start_time
      or new.end_time <> old.end_time
      or new.activity <> old.activity
      or new.professor_name <> old.professor_name
    ) then
      raise exception using errcode = '42501', message = 'Solo la administración puede modificar los datos de una reserva';
    end if;
    if old.status = 'active' and new.status = 'cancelled' then
      new.cancelled_at = now();
    end if;
  end if;

  return new;
end;
$$;

create trigger reservations_protect_fields
before insert or update on public.reservations
for each row execute function public.protect_reservation_fields();

