create extension if not exists btree_gist with schema extensions;

create type public.user_role as enum ('teacher', 'admin');
create type public.reservation_status as enum ('active', 'cancelled');

create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null unique check (email ~ '^[a-z0-9-]+\.[a-z0-9-]+\.[a-z0-9-]+@una\.cr$'),
  full_name text not null check (char_length(trim(full_name)) between 3 and 100),
  role public.user_role not null default 'teacher',
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.classrooms (
  id bigint generated by default as identity primary key,
  code text not null unique,
  name text not null,
  active boolean not null default true,
  sort_order integer not null default 0
);

create table public.fixed_occupancies (
  id bigint generated by default as identity primary key,
  classroom_id bigint not null references public.classrooms(id) on delete cascade,
  day_of_week smallint not null check (day_of_week between 0 and 6),
  start_time time not null,
  end_time time not null,
  label text not null,
  check (start_time < end_time)
);

create table public.reservations (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete restrict,
  classroom_id bigint not null references public.classrooms(id) on delete restrict,
  reservation_date date not null,
  start_time time not null,
  end_time time not null,
  activity text not null check (char_length(trim(activity)) between 3 and 120),
  professor_name text not null check (char_length(trim(professor_name)) between 3 and 100),
  status public.reservation_status not null default 'active',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  cancelled_at timestamptz,
  check (start_time < end_time),
  check (start_time >= time '07:00' and end_time <= time '21:30'),
  check (reservation_date between date '2026-07-20' and date '2026-12-20'),
  exclude using gist (
    classroom_id with =,
    tsrange(reservation_date + start_time, reservation_date + end_time, '[)') with &&
  ) where (status = 'active')
);

create index reservations_user_date_idx on public.reservations(user_id, reservation_date);
create index reservations_date_room_idx on public.reservations(reservation_date, classroom_id);
create index fixed_occupancies_room_day_idx on public.fixed_occupancies(classroom_id, day_of_week);

