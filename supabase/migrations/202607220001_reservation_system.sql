create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.profiles
    where id = auth.uid() and role = 'admin' and active = true
  );
$$;

create policy "Aulas visibles para todos"
on public.classrooms for select
using (active = true);

create policy "Ocupaciones fijas visibles para todos"
on public.fixed_occupancies for select
using (true);

create policy "Perfil propio o administración"
on public.profiles for select
to authenticated
using (id = auth.uid() or public.is_admin());

create policy "Administración actualiza perfiles"
on public.profiles for update
to authenticated
using (public.is_admin())
with check (public.is_admin());

create policy "Reservas activas visibles para todos"
on public.reservations for select
using (status = 'active' or user_id = auth.uid() or public.is_admin());

create policy "Docentes crean sus reservas"
on public.reservations for insert
to authenticated
with check (
  user_id = auth.uid()
  and exists (select 1 from public.profiles where id = auth.uid() and active = true)
);

create policy "Propietarios cancelan sus reservas"
on public.reservations for update
to authenticated
using (user_id = auth.uid() and status = 'active')
with check (user_id = auth.uid() and status = 'cancelled');

create policy "Administración gestiona todas las reservas"
on public.reservations for update
to authenticated
using (public.is_admin())
with check (public.is_admin());

